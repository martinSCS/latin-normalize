# 类别的二分查找
def TBS(arr, l, r, x): 
    if r >= l: 
        mid = int(l + (r - l) / 2)
        if mid == 581: 
            return mid-1
        if arr[mid][0] == x: 
            return mid
        elif arr[mid][0] > x: 
            return TBS(arr, l, mid-1, x)
        else: 
            return TBS(arr, mid+1, r, x)
    else: 
        return r

# 获取类别信息
def getClass(x): 
    if ord(x) > 1114111: 
        return None
    index = TBS(list(dict_ccc.keys()), 0, len(dict_ccc), ord(x))
    return dict_ccc[list(dict_ccc.keys())[index]]

# 获取类别代码
def getCode(x): 
    return dict_ccc_code[getClass(x)]

# 获取十六进制U32
def getU32(x): 
    u32 = hex(ord(x))[2:].upper()
    while len(u32) < 4: 
        u32 = '0' + u32
    return u32

# 获取十进制U32，返回整型
def getU32_dec(x):
    return int(getU32(x),16)

# 将十六进制U32转回字符
def parseU32(u32): 
    return chr(int(u32, 16))

# 以字符与其附标为组合切割成列表
def splitText(text): 
    chrList = []
    temp = ''
    for i in text: 
        if getCode(i) == 0 and len(temp) != 0: 
            chrList.append(temp)
            temp = ''
        temp += i
    if len(temp) != 0: 
        chrList.append(temp)
    return chrList

# 将完整字符按照canonical规则彻底拆分
def apart(x): 
    text = x
    if getU32(x) in dict_dm.keys(): 
        dm = dict_dm[getU32(x)]
        text = ''
        for i in dm.split(' '): 
            text += apart(parseU32(i))
    return text

# 将可以canonical组合的字符组合按顺序组合（仅一次）
def single_join(text):
    pre = []
    for i in text: 
        pre.append(getU32(i))
    for i in range(len(pre)):
        sub = pre[:len(pre)-i]
        substr = ' '.join(sub)
        if substr in dict_dm_reverse.keys() or len(sub) == 1: 
            if substr in dict_dm_reverse.keys(): 
                pre = [dict_dm_reverse[substr]] + pre[len(sub):]
            else: 
                pre = [substr] + pre[len(sub):]
    result = ''
    for i in pre:
        result += parseU32(i)
    return result

# 将可以canonical组合的字符按顺序组合（彻底）
def join(text): 
    pre = text
    for i in text: 
        pre = single_join(pre)
    return pre

# 快速排序判断函数
def charQSpartition(arr,low,high): 
    i = ( low-1 )
    pivot = getCode(arr[high])
    for j in range(low , high): 
        if   getCode(arr[j]) <= pivot: 
            i = i+1
            arr[i],arr[j] = arr[j],arr[i]
    arr[i+1],arr[high] = arr[high],arr[i+1]
    return ( i+1 )

# 快速排序
def charquickSort(arr,low,high): 
    if low < high: 
        pi = charQSpartition(arr,low,high) 
        charquickSort(arr, low, pi-1) 
        charquickSort(arr, pi+1, high) 

# 将字符拆解并按照CCC组合
def sort(text, toList=False):
    textlist = splitText(text)
    resList = []
    for i in textlist: 
        temp = []
        for j in i: 
            temp += apart(j)
        charquickSort(temp,0,len(temp)-1)
        resList.append(''.join(temp))
    if toList: 
        return resList
    return ''.join(resList)

# 将所有拉丁字母规范化
def normalize(text, toList=False):
    pre = sort(text, True)
    res = []
    for i in pre: 
        res.append(join(i))
    if toList:
        return res
    return ''.join(res)